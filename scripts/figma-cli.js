// Command handlers
function handleConfig(args) {
  const [action, key, value] = args;
  const config = loadConfig();

  switch (action) {
    case 'set':
      if (key && value) {
        config[key] = value;
        saveConfig(config);
      } else {
        console.log('Usage: figma config set <key> <value>');
      }
      break;
      
    case 'get':
      if (key) {
        console.log(`${key}: ${config[key] || 'not set'}`);
      } else {
        console.log('Current configuration:');
        console.log(JSON.stringify(config, null, 2));
      }
      break;
      
    case 'list':
      console.log('Current configuration:');
      console.log(JSON.stringify(config, null, 2));
      break;
      
    default:
      console.log(`
Figma Configuration Commands:
  figma config set <key> <value>  - Set configuration value
  figma config get <key>          - Get configuration value  
  figma config list               - List all configuration
  
Example:
  figma config set accessToken your-figma-token
  figma config set fileKey your-file-key
`);
  }
}

function handleExport(args) {
  const [format, filename] = args;
  
  switch (format) {
    case 'tokens':
      const tokens = generateTokensFile();
      const tokensFile = filename || 'design-tokens.json';
      fs.writeFileSync(tokensFile, tokens);
      console.log(`‚úÖ Design tokens exported to ${tokensFile}`);
      break;
      
    case 'variables':
      const variables = generateVariablesFile();
      const variablesFile = filename || 'figma-variables.json';
      fs.writeFileSync(variablesFile, variables);
      console.log(`‚úÖ Figma variables exported to ${variablesFile}`);
      break;
      
    case 'tokens-studio':
      const tokensStudio = generateTokensStudioFile();
      const tokensStudioFile = filename || 'tokens-studio.json';
      fs.writeFileSync(tokensStudioFile, tokensStudio);
      console.log(`‚úÖ Tokens Studio format exported to ${tokensStudioFile}`);
      break;
      
    default:
      console.log(`
Export Commands:
  figma export tokens [filename]       - Export design tokens JSON
  figma export variables [filename]    - Export Figma variables JSON
  figma export tokens-studio [filename] - Export Tokens Studio format JSON
  
Examples:
  figma export tokens
  figma export variables my-variables.json
  figma export tokens-studio
`);
  }
}

function handleSync(args) {
  const [action] = args;
  const config = loadConfig();
  
  if (!config.accessToken || !config.fileKey) {
    console.log('‚ùå Figma configuration missing. Run:');
    console.log('  figma config set accessToken your-token');
    console.log('  figma config set fileKey your-file-key');
    return;
  }
  
  switch (action) {
    case 'push':
      console.log('‚ö†Ô∏è  Push to Figma requires "Read and write" permissions for File content.');
      console.log('');
      console.log('Your current token has "Read only" permissions, which is perfect for:');
      console.log('‚Ä¢ Pulling styles from Figma to code');
      console.log('‚Ä¢ Exporting design tokens');
      console.log('‚Ä¢ Reading Figma file data');
      console.log('');
      console.log('üí° Alternative: Export design tokens and import them via Figma plugins:');
      console.log('   figma export tokens');
      console.log('   figma export variables');
      break;
      
    case 'pull':
      console.log('üîÑ Pulling design system from Figma...');
      console.log('üì• This would import styles from Figma to your code');
      console.log('‚úÖ Pull completed (simulated)');
      break;
      
    case 'status':
      console.log('üìä Figma Sync Status:');
      console.log(`  Access Token: ${config.accessToken ? '‚úÖ Set' : '‚ùå Missing'}`);
      console.log(`  File Key: ${config.fileKey ? '‚úÖ Set' : '‚ùå Missing'}`);
      console.log(`  Auto Sync: ${config.autoSync ? 'üîÑ Enabled' : '‚è∏Ô∏è Disabled'}`);
      break;
      
    default:
      console.log(`
Sync Commands:
  figma sync push    - Push design system to Figma
  figma sync pull    - Pull design system from Figma  
  figma sync status  - Show sync status
  
Examples:
  figma sync push
  figma sync pull
`);
  }
}

function handleInit() {
  console.log('üé® Initializing Figma integration...');
  
  // Create basic config structure
  const config = {
    accessToken: '',
    fileKey: '',
    autoSync: false,
    syncInterval: 30000
  };
  
  saveConfig(config);
  
  console.log(`
‚úÖ Figma integration initialized!

Next steps:
1. Get your Figma access token:
   - Go to Figma ‚Üí Settings ‚Üí Personal access tokens
   - Generate new token with "File content" permissions
   
2. Configure your credentials:
   figma config set accessToken YOUR_TOKEN
   figma config set fileKey YOUR_FILE_KEY
   
3. Test the connection:
   figma sync status
   
4. Push your design system:
   figma sync push
`);
}

// Main CLI handler
function main() {
  const args = process.argv.slice(2);
  const command = args[0];
  const subArgs = args.slice(1);
  
  switch (command) {
    case 'init':
      handleInit();
      break;
      
    case 'config':
      handleConfig(subArgs);
      break;
      
    case 'export':
      handleExport(subArgs);
      break;
      
    case 'sync':
      handleSync(subArgs);
      break;
      
    case 'help':
    case '--help':
    case '-h':
    default:
      console.log(`
üé® Figma Design System CLI

Usage:
  figma <command> [options]

Commands:
  init                 - Initialize Figma integration
  config <action>      - Manage configuration
  export <format>      - Export design tokens/variables
  sync <action>        - Sync with Figma
  help                 - Show this help message

Examples:
  figma init
  figma config set accessToken your-token
  figma export tokens
  figma sync push

For detailed help on a command:
  figma <command> help
`);
  }
}

// Run the CLI
main(); 